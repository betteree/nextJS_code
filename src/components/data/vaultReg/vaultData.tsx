export function isVaultCode(vaultCode: string, gender: string) {
  let isValid = false;

  const value = Number(vaultCode);

  if (gender === "여") {
    isValid = female_vault_codes.includes(value);
  } else if (gender === "남") {
    isValid = male_vault_codes.includes(value);
  }

  return isValid;
}
